// ==========================================
// 将所有剧情写入关卡数据
// ==========================================
function applyStories(chapter, stories) {
  if (!chapter.levels) return;
  for (const level of chapter.levels) {
    const s = stories[level.levelId];
    if (s) {
      if (s.preDialog) level.preDialog = s.preDialog;
      if (s.clearDialog) level.clearDialog = s.clearDialog;
    }
  }
}

